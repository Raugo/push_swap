#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	srcsize;

	srcsize = ft_strlen((char *)src);
	if (srcsize + 1 < size)
		ft_memcpy(dest, src, srcsize + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = 0;
	}
	return (srcsize);
}
